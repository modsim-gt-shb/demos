md"""
# Logistic model (Julia version) #

Let's solve the dynamical system for $x(t)$ given by the logistic model,

``
  D x = r \left( 1 - \frac{x}{k} \right) x.
``

where ``r`` is the intrinsic population growth rate (unconstrained) and ``k`` is the maximum capacity of the system that can by its available resources. For example, ``x(t)`` might be the number of sheep living on a farm that can only sustainably support up to ``k`` sheep.

> This model is due to Velhurst. **Citation** (per Wikipedia): Verhulst, Pierre-François (1838). "Notice sur la loi que la population suit dans son accroissement". _Correspondance mathématique et physique_. 10: 113–121. [[Click here for an English translation](https://link.springer.com/content/pdf/10.1007/BF02309004.pdf)].
"""

# ╔═╡ f5fb558b-0cc4-4846-b466-ea62db88f7f6
md"""
# Implementation #

The following code was adapted from the following tutorial: [link](https://diffeq.sciml.ai/stable/tutorials/ode_example/).
"""

# ╔═╡ f78798b4-e693-49b3-943b-5529ae50dcc3
md"""
The right-hand side of the model is encoded in the function, `F_logistic(dx, x, p, t)`, below. The arguments are:
- `t`: The current time, a scalar value.
- `x`: The state variable at time `t` as a one-element vector.
- `p`: The model parameters as a two-element vector `[r; k]`.
- `dx`: The instantaneous derivatives at time `t` as a one-element vector.
"""

# ╔═╡ 6b099ceb-da77-44e5-b1fc-73fdb83394aa
md"""
Recall:

``
D x = r \left( 1 - \frac{x}{k} \right) x.
``
"""

# ╔═╡ 4e3d2e84-d722-44f6-b22b-ac274cdd4dc8
function F_logistic!(dx, x, p, t)
	r, k = p
    dx[1] = r * (1 - x[1]/k) * x[1]
end

# ╔═╡ 3c512f0d-8024-4076-87a6-8eb6935f9cf8
md"""
The following cell invokes an ODE solver to solve the system with a given set of model parameters (`p`) and initial conditions (`x0`).
"""

# ╔═╡ c32a6dde-e78b-4550-891b-f619d784b926
# Model parameters: p = [r; k]
p = [0.10; 100]

# ╔═╡ ac630de6-062c-40f8-9388-14147f72e85a
# Initial conditions: x0
x0 = [10]

# ╔═╡ be9e87cd-ebd8-448a-8c72-11395bbc27fd
# Length of time for the (numerical) simulation: tspan = (t_initial, t_final)
tspan = (0, 100)

# ╔═╡ 4f328a81-f119-467c-b0fe-9fcf746af681
# "Wrap" these components into a problem instance:
prob = ODEProblem(F_logistic!, x0, tspan, p)

# ╔═╡ b0724155-b584-48d6-8dcc-7e8781c05581
# Solve the problem:
sol = solve(prob)

# ╔═╡ 68268cc3-cfc5-4fa3-a11e-baec740e215c
# Helper functions to extract solution components (i.e., the time points and solution values)
get_t(sol) = sol.t

# ╔═╡ 0e17ff31-cc43-41f7-bb2c-faea061e0783
get_u(sol, col_id=1) = [u[col_id] for u in sol.u]

# ╔═╡ d56b0d13-b7bb-46bb-8601-234b6f99b8b2
function plot_sol(t, x, x0, p; title="Logistic model")
    set_default_plot_size(640px, 360px)
    plot(x=t, y=x, Geom.point, Geom.line,
         yintercept=[p[2]], Geom.hline(color="grey", style=:dash),
         Guide.xlabel("t"), Guide.ylabel("x(t)", orientation=:horizontal),
         Guide.title("$title: x(0)=$(x0[1]), r=$(p[1]), k=$(p[2])"))
end

# ╔═╡ 761f6cf9-e143-4fff-8e45-076c5a27fc4b
plot_sol(get_t(sol), get_u(sol), x0, p)

# ╔═╡ c449fc9c-b3e6-4fa4-b449-c1bc6ccd8319
md"""
# Variable rescaling #

If you apply the technique of variable scaling, you can reduce the original model to the parameter-free form,

``\hat{D}\hat{x} = \hat{x}(1 - \hat{x}),``

with the substitutions ``x = k \hat{x}`` and ``t = \frac{\hat{t}}{r}``.
"""

# ╔═╡ 95eb1ecc-565c-446f-8b1c-b1970a27034e
function rescale(x, t, p)
	r, k = p
	x_hat = (x != nothing) ? x ./ k : x
	t_hat = (t != nothing) ? t .* r : t
	x_hat, t_hat
end

# ╔═╡ 89f0c9ea-173f-44fb-b58e-0d51cb42f939
function unscale(x_hat, t_hat, p)
	r, k = p
	x = (x_hat != nothing) ? x_hat .* k : x_hat
	t = (t_hat != nothing) ? t_hat ./ r : t_hat
	x, t
end

# ╔═╡ 7ae398aa-ed51-408a-bb98-0945d313bc49
p_hat = [1.0; 1.0] # eliminated parameters

# ╔═╡ 470d9c96-d23a-4828-9a2b-9b3a1af1371b
x0_hat, tspan_hat = rescale(x0, tspan, p)

# ╔═╡ 710dd937-ab57-4b1f-81f0-566f4c945a3f
prob_hat = ODEProblem(F_logistic!, x0_hat, tspan_hat, p_hat)

# ╔═╡ b8ec486e-75ac-49ae-994d-08dd0b93900e
sol_hat = solve(prob_hat)

# ╔═╡ bea75646-661a-4f64-a6ec-e7646df52927
plot_sol(get_t(sol_hat), get_u(sol_hat), x0_hat, p_hat, title="Scaled solution")

# ╔═╡ 5e8ad7b6-2afb-4016-be72-8d695b816404
x_unscaled, t_unscaled = unscale(get_u(sol_hat), get_t(sol_hat), p)

# ╔═╡ 855e6c51-6062-42f3-88b9-f1a38cde57d6
plot_sol(t_unscaled, x_unscaled, x0, p,
	     title="Unscaled solution derived from the scaled one")

